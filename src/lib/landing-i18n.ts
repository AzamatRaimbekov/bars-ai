import { create } from 'zustand'

export type LandingLang = 'ru' | 'en' | 'ky' | 'uz'

export const LANG_LABELS: Record<LandingLang, string> = {
  ru: 'RU',
  en: 'EN',
  ky: 'KY',
  uz: 'UZ',
}

interface LandingLangStore {
  lang: LandingLang
  setLang: (lang: LandingLang) => void
}

const saved = (typeof localStorage !== 'undefined' ? localStorage.getItem('landing_lang') : null) as LandingLang | null

export const useLandingLang = create<LandingLangStore>((set) => ({
  lang: saved && ['ru', 'en', 'ky', 'uz'].includes(saved) ? saved : 'ru',
  setLang: (lang) => {
    localStorage.setItem('landing_lang', lang)
    set({ lang })
  },
}))

export function t(key: string): string {
  const lang = useLandingLang.getState().lang
  return translations[lang]?.[key] ?? translations['ru'][key] ?? key
}

