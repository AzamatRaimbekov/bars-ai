def video(title: str, url: str) -> dict:
    return {"type": "video", "title": title, "url": url}


def info(title: str, md: str) -> dict:
    return {"type": "info", "title": title, "markdown": md}


def tf(statement: str, correct: bool) -> dict:
    return {"type": "true-false", "statement": statement, "correct": correct}


def quiz(question: str, options: list[tuple[str, bool]]) -> dict:
    return {
        "type": "quiz",
        "question": question,
        "options": [
            {"id": chr(ord("a") + i), "text": text, "correct": ok}
            for i, (text, ok) in enumerate(options)
        ],
    }


def multi(question: str, options: list[tuple[str, bool]]) -> dict:
    return {
        "type": "multi-select",
        "question": question,
        "options": [
            {"id": chr(ord("a") + i), "text": text, "correct": ok}
            for i, (text, ok) in enumerate(options)
        ],
    }


def order(items: list[str]) -> dict:
    return {"type": "drag-order", "items": items}


def match(pairs: list[tuple[str, str]]) -> dict:
    return {
        "type": "matching",
        "pairs": [{"left": l, "right": r} for l, r in pairs],
    }


def sort_items(categories: list[tuple[str, list[str]]]) -> dict:
    return {
        "type": "category-sort",
        "categories": [{"name": n, "items": items} for n, items in categories],
    }


def cards(pairs: list[tuple[str, str]]) -> dict:
    return {
        "type": "flashcards",
        "cards": [{"front": f, "back": b} for f, b in pairs],
    }


